const os= require('os');

// Platform
console.log(os.platform());

// CPU architecture
console.log(os.arch());

// CPU core information
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Home Directory
console.log(os.homedir());

// Hostname
console.log(os.hostname());

// Uptime
console.log(os.uptime());